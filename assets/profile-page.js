const API_BASE = (typeof API_CONFIG !== 'undefined' && API_CONFIG.BASE_URL)
            ? API_CONFIG.BASE_URL
            : 'https://canadaaccountants-backend-production-1d8f.up.railway.app';

        function getProfileId() {
            // Support /profile?id=123 and /profile/123
            const params = new URLSearchParams(window.location.search);
            if (params.get('id')) return params.get('id');
            const parts = window.location.pathname.split('/');
            const idx = parts.indexOf('profile');
            if (idx !== -1 && parts[idx + 1]) return parts[idx + 1];
            return null;
        }

        function getInitials(name) {
            return name.split(' ').filter(Boolean).map(w => w[0]).join('').toUpperCase().slice(0, 2);
        }

        function getScoreColor(score) {
            if (score >= 80) return '#059669';
            if (score >= 60) return '#2563eb';
            if (score >= 40) return '#f59e0b';
            return '#ef4444';
        }

        function renderError(msg) {
            document.getElementById('page-content').innerHTML = `
                <div class="page-error">
                    <i class="fas fa-exclamation-circle"></i>
                    <h2>Profile Not Found</h2>
                    <p>${msg}</p>
                    <p style="margin-top: 16px;"><a href="find-cpa">Find a CPA instead</a></p>
                </div>`;
        }

        function renderProfile(data) {
            var seoPre = document.getElementById('seoPrerender'); if (seoPre) seoPre.style.display = 'none';
            const p = data.profile;
            const seo = data.seo_score || { score: 0, grade: 'D', recommendations: [] };
            const scoreColor = getScoreColor(seo.score);

            // Update page title and meta
            document.title = `${p.name} — CPA Profile | CanadaAccountants.app`;
            const metaDesc = document.querySelector('meta[name="description"]');
            if (metaDesc) metaDesc.content = `View ${p.name}'s CPA profile on CanadaAccountants.app. ${p.designation || 'CPA'} in ${[p.city, p.province].filter(Boolean).join(', ') || 'Canada'}.`;

            // Inject JSON-LD
            if (data.structured_data) {
                document.getElementById('jsonld-script').textContent = JSON.stringify(data.structured_data);
            }

            // Designation badges
            const designations = (p.designation || '').split(/[,\/]/).map(d => d.trim()).filter(Boolean);
            const designationBadges = designations.length
                ? designations.map(d => `<span class="badge badge-designation"><i class="fas fa-award"></i> ${d}</span>`).join('')
                : '<span class="badge badge-designation"><i class="fas fa-user-tie"></i> CPA</span>';

            // Claim badge
            const claimBadge = p.claimed
                ? '<span class="badge badge-claimed"><i class="fas fa-check-circle"></i> Verified Profile</span>'
                : '<span class="badge badge-unclaimed"><i class="fas fa-info-circle"></i> Unclaimed &mdash; Basic Listing</span>';

            // Firm display
            const firmDisplay = p.firm_name || `Listed in ${p.province || 'Canadian'} CPA directory`;

            // Location
            const location = [p.city, p.province].filter(Boolean).join(', ');

            // Bio section
            let bioHtml;
            if (p.bio) {
                bioHtml = `<div class="bio-text">${p.bio.replace(/\n\n/g, '</p><p class="bio-text" style="margin-top:12px;">').replace(/\n/g, '<br>')}</div>
                    <p class="bio-note"><i class="fas fa-info-circle"></i> Generated by AI from CPA directory data</p>`;
            } else {
                bioHtml = `<div class="bio-loading"><i class="fas fa-spinner fa-spin"></i> Bio is being generated...</div>`;
            }

            // Recommendations
            const recHtml = seo.recommendations.slice(0, 5).map(r => {
                const iconClass = r.impact === 'high' ? 'rec-high' : r.impact === 'medium' ? 'rec-medium' : 'rec-low';
                return `<li class="recommendation-item">
                    <span class="rec-icon ${iconClass}"><i class="fas fa-plus"></i></span>
                    <span>${r.action}</span>
                    <span class="rec-points">+${r.points} pts</span>
                </li>`;
            }).join('');

            // CTA link
            const claimUrl = p.ref_token
                ? `claim-profile?ref=${p.ref_token}`
                : `claim-profile`;

            document.getElementById('page-content').innerHTML = `
                <!-- Profile Hero -->
                <section class="profile-hero">
                    <div class="profile-avatar">${getInitials(p.name)}</div>
                    <h1 class="profile-name">${p.name}${p.founding_member ? '<span style="display:inline-block;background:linear-gradient(135deg,#f59e0b,#d97706);color:#fff;font-size:11px;font-weight:700;padding:4px 10px;border-radius:12px;margin-left:8px;text-transform:uppercase;letter-spacing:0.5px;">Founding Member</span>' : ''}</h1>
                    <p class="profile-firm">${firmDisplay}</p>
                    ${location ? `<p class="profile-location"><i class="fas fa-map-marker-alt"></i> ${location}</p>` : ''}
                    <div class="badges">
                        ${designationBadges}
                        ${claimBadge}
                    </div>
                </section>

                <div class="profile-content">
                    <!-- AI Bio -->
                    <div class="section-card">
                        <div class="section-header">
                            <div class="section-icon"><i class="fas fa-robot"></i></div>
                            <div>
                                <div class="section-title">AI-Generated Professional Bio</div>
                                <div class="section-subtitle">Created from CPA directory data</div>
                            </div>
                        </div>
                        ${bioHtml}
                    </div>

                    <!-- Profile Score -->
                    <div class="section-card">
                        <div class="section-header">
                            <div class="section-icon"><i class="fas fa-chart-line"></i></div>
                            <div>
                                <div class="section-title">Profile Score</div>
                                <div class="section-subtitle">How complete and optimized this profile is</div>
                            </div>
                        </div>
                        <div class="score-section">
                            <div class="score-circle-container">
                                <div class="score-circle" style="--score: ${seo.score}; background: conic-gradient(${scoreColor} ${seo.score * 3.6}deg, rgba(255,255,255,0.08) 0deg);">
                                    <div class="score-circle-inner">
                                        <div class="score-number" style="color: ${scoreColor};">${seo.score}</div>
                                        <div class="score-label">/ 100</div>
                                    </div>
                                </div>
                                <div class="score-grade">Grade ${seo.grade}</div>
                            </div>
                            <div class="score-details">
                                ${seo.recommendations.length > 0 ? `
                                    <h4>What's Missing</h4>
                                    <ul class="recommendation-list">${recHtml}</ul>
                                    <p class="score-cta-text">Take control of this profile to improve the score.</p>
                                ` : `
                                    <h4>Excellent!</h4>
                                    <p style="color: var(--text-secondary);">This profile is fully optimized.</p>
                                `}
                            </div>
                        </div>
                    </div>

                    <!-- CTA -->
                    <div class="section-card cta-section">
                        <h2>Take Control of Your Profile</h2>
                        <p>Edit your bio, add specializations, and manage what clients see.</p>
                        <a href="${claimUrl}" class="btn-cta">Take Control <i class="fas fa-arrow-right"></i></a>
                        ${!p.claimed ? `<div style="margin-top:14px;"><button id="organicClaimBtn" data-pid="${p.id || getProfileId() || ''}" class="btn-cta" style="background:transparent;border:1px solid var(--primary-blue,#2563eb);color:var(--primary-blue,#2563eb);cursor:pointer;">Is this you? Claim this profile</button><div id="organicClaimMsg" style="display:none;margin-top:10px;font-size:14px;color:#10b981;"></div></div>` : ''}
                    </div>
                </div>`;

            // Organic claim-capture: record the intent (and alert admin) via /api/claim/attempt,
            // then continue to the claim/verify flow. Capture is gated on HTTP 2xx; on failure the
            // visitor is still pointed at the Take Control link, never a false "recorded" state.
            const organicBtn = document.getElementById('organicClaimBtn');
            if (organicBtn) {
                organicBtn.addEventListener('click', async function() {
                    const pid = organicBtn.getAttribute('data-pid') || getProfileId();
                    const msg = document.getElementById('organicClaimMsg');
                    organicBtn.disabled = true;
                    let ok = false;
                    try {
                        const r = await fetch('https://canadaaccountants-backend-production-1d8f.up.railway.app/api/claim/attempt', {
                            method: 'POST', headers: { 'Content-Type': 'application/json' },
                            body: JSON.stringify({ pid: pid, source_page: 'profile' })
                        });
                        ok = r.ok;
                        if (!ok) console.error('claim attempt failed with status', r.status);
                    } catch (e) {
                        console.error('claim attempt error', e);
                        ok = false;
                    }
                    if (ok) {
                        window.location.href = claimUrl;
                    } else {
                        organicBtn.disabled = false;
                        if (msg) { msg.textContent = 'Could not record that. Use the Take Control button above to claim your profile.'; msg.style.color = '#f59e0b'; msg.style.display = 'block'; }
                    }
                });
            }
        }

        // Load profile
        (async function() {
            const id = getProfileId();
            if (!id) {
                renderError('No profile ID provided. Try visiting a profile link from the directory.');
                return;
            }

            try {
                const res = await fetch(`${API_BASE}/api/profiles/${id}`);
                if (!res.ok) {
                    const err = await res.json().catch(() => ({}));
                    renderError(err.error || 'This profile could not be found.');
                    return;
                }
                const data = await res.json();
                renderProfile(data);

                // Render related profiles for internal SEO linking
                if (data.related && data.related.length > 0) {
                    const province = data.profile.province || '';
                    const relatedHtml = data.related.map(r => `
                        <a href="/profile?id=${r.id}" style="display:block;padding:16px;margin:8px 0;background:rgba(255,255,255,0.03);border:1px solid #333;border-radius:8px;text-decoration:none;color:#fff;transition:border-color 0.3s;">
                            <strong style="color:#2563eb;">${r.name}</strong>
                            ${r.firm ? `<span style="color:#999;margin-left:8px;">${r.firm}</span>` : ''}
                            <br><span style="color:#888;font-size:13px;">${r.designation || ''} ${r.city ? '— ' + r.city + ', ' + r.province : r.province || ''}</span>
                        </a>
                    `).join('');

                    const container = document.querySelector('.profile-content') || document.querySelector('.container') || document.querySelector('main');
                    if (container) {
                        const section = document.createElement('div');
                        section.style.cssText = 'max-width:800px;margin:40px auto;padding:0 20px;';
                        section.innerHTML = `
                            <h3 style="color:#fff;font-size:18px;margin:0 0 16px;font-weight:600;">Other Accountants in ${province}</h3>
                            ${relatedHtml}
                        `;
                        container.appendChild(section);
                    }
                }
            } catch (err) {
                renderError('Unable to load this profile. Please try again later.');
            }
        })();
